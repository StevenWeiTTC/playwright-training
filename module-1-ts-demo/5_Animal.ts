class Animal {
    // Properties
    private name: string;
    private age: number;
    private species: string;

    // Constructor
    constructor(name: string, age: number, species: string) {
        this.name = name;
        this.age = age;
        this.species = species;
    }

    // Methods
    // Getters and Setters
    getName(): string {
        return this.name;
    }

    setName(name: string): void {
        this.name = name;
    }

    getAge(): number {
        return this.age;
    }

    setAge(age: number): void {
        this.age = age;
    }

    getSpecies(): string {
        return this.species;
    }

    setSpecies(species: string): void {
        this.species = species;
    }

    // Other methods
    makeSound(): void {
        console.log("Animal makes a sound");
    }
}

const dog = new Animal("Pluto", 2, "Dog");

console.log(`${dog.getName()} is ${dog.getAge()} years old!`);

dog.setAge(3);

console.log(`${dog.getName()} is now ${dog.getAge()} years old!`);
